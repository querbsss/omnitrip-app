  static List<String> get islands => const ['Luzon', 'Visayas', 'Mindanao'];

  static Origin? byId(String? id) {
    if (id == null) return null;
    for (final o in all) {
      if (o.id == id) return o;
    }
    return null;
  }

  static Origin? byName(String? name) {
    if (name == null || name.trim().isEmpty) return null;
    final needle = name.trim().toLowerCase();
    for (final o in all) {
      if (o.name.toLowerCase() == needle) return o;
      if (o.displayName.toLowerCase() == needle) return o;
    }
    return null;
  }

  static List<Origin> byIsland(String island) =>
      all.where((o) => o.island == island).toList();
